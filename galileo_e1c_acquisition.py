import numpy as np


class GalileoE1CAcquisition:
    """
    Galileo E1C (pilot) acquisition engine.

    Notes:
    - E1C pilot primary code length: 4092 chips at 1.023 Mcps (≈4.0 ms)
    - Secondary code length: 25 chips at 250 Hz (period 100 ms)
    - Modulation on E1 is CBOC(6,1,1/11). For acquisition we correlate with
      the primary code; BOC/CBOC subcarrier shaping can be optionally applied
      to replicas but is not strictly necessary for coarse acquisition.

    This implementation mirrors the GPS acquisition structure already present
    in the project, adapted to the E1C primary code length and search settings.

    Important:
    - The true E1C primary codes are generated via the Weil construction over
      GF(p) with p = 4093 and PRN-dependent parameters (d, p0). Implementing
      the exact ICD generator requires the official parameter tables. Here we
      implement a standards-compliant Weil generator scaffold and provide a
      deterministic per-PRN parameterization so that the generator is fully
      offline and self-consistent with the included signal generator. This is
      sufficient for end-to-end testing with the provided generator and for
      lab validation. To acquire real satellites, replace
      `_get_weil_parameters_for_prn` with the official (d, p0) tables.
    """

    def __init__(self,
                 sampling_freq=2.4e6,
                 IF=0.0,
                 acq_satellite_list=None,
                 acq_search_band=14.0,
                 acq_threshold=2.0):
        self.sampling_freq = float(sampling_freq)
        self.IF = float(IF)
        self.acq_threshold = float(acq_threshold)
        self.acq_search_band = float(acq_search_band)  # kHz, around IF

        # PRN list: Galileo E1 typically uses PRN 1..36 (and more for reserves).
        if acq_satellite_list is None:
            self.acq_satellite_list = list(range(1, 37))
        else:
            self.acq_satellite_list = list(acq_satellite_list)

        # E1C primary code constants
        self.code_freq_basis = 1.023e6
        self.primary_code_length_chips = 4092
        self.prime_p = 4093  # prime used in construction

        # Precompute primary code table (digitized at sampling_freq)
        self.ca_codes_table = self._make_primary_table()

    # -----------------------------
    # Primary code generation (Weil)
    # -----------------------------
    def _legendre_symbol(self, a, p):
        """Compute the Legendre symbol (a|p) for prime p.

        Returns 1 if a is a quadratic residue mod p, -1 if non-residue,
        and 0 if a ≡ 0 (mod p).
        """
        a_mod = a % p
        if a_mod == 0:
            return 0
        # pow(a, (p-1)//2, p) ∈ {1, p-1}; map p-1 -> -1
        val = pow(a_mod, (p - 1) // 2, p)
        return -1 if val == p - 1 else 1

    def _weil_sequence(self, d, p0):
        """Generate Weil sequence s[n] for n=0..4091 using GF(p) with p=4093.

        Reference scaffold: s[n] = L(n + d) * L(n) where L(x) is Legendre symbol.
        This yields a 4092-long binary sequence in {+1,-1} after dropping the 0
        at n corresponding to x=0 mod p.
        """
        p = self.prime_p
        seq = np.empty(self.primary_code_length_chips, dtype=np.int8)

        # Map chip index k in 0..4091 to field element x = k+1 (skip 0)
        # Apply PRN-dependent shift p0 to index as x' = (x + p0) mod p
        for k in range(self.primary_code_length_chips):
            x = (k + 1) % p
            x_shift = (x + p0) % p
            # L(x_shift) and L(x_shift + d)
            Lx = self._legendre_symbol(x_shift, p)
            Lxd = self._legendre_symbol((x_shift + d) % p, p)
            v = Lx * Lxd
            # Map 0 -> +1 by convention (should rarely occur except when x' or x'+d ≡ 0)
            if v == 0:
                v = 1
            seq[k] = 1 if v > 0 else -1
        return seq.astype(np.int8)

    def _hex_to_binary(self, hex_string):
        """Convert hex string to binary array"""
        # Remove any whitespace and convert to uppercase
        hex_string = hex_string.replace(' ', '').upper()
        
        # Convert hex to binary string
        binary_string = bin(int(hex_string, 16))[2:]  # Remove '0b' prefix
        
        # Pad to ensure we have the right length (4092 bits)
        while len(binary_string) < 4092:
            binary_string = '0' + binary_string
        
        # Convert to numpy array of +/-1
        binary_array = np.array([1 if bit == '1' else -1 for bit in binary_string[-4092:]])
        return binary_array

